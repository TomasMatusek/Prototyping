package com.matusek.basic;

public class C05_Numbers {
    public static void main(String[] args) {

        // Base 10
        System.out.println(56);

        // Octal (digits 0–7), which uses the number 0 as a prefix
        System.out.println(0b11);

        // Hexadecimal (digits 0–9 and letters A–F), which uses the number 0 followed by x or X as a prefix
        System.out.println(017);

        // Binary (digits 0–1), which uses the number 0 followed by b or B as a prefix
        System.out.println(0x1F);
    }
}
